def build_summary_tab(ws: Any, plan: Dict[str, Any]) -> None:
    """Build the Summary tab."""
    import gspread
    from gspread.utils import rowcol_to_a1

    client = plan["client"]
    eng = plan["engagement"]

    # Header block
    header_data = [
        ["Client", client["name"]],
        ["Engagement", eng["name"]],
        ["Start Date", eng["startDate"]],
        ["End Date", eng["estimatedEndDate"]],
        ["Total Hours", f"{eng['totalHours']['min']}-{eng['totalHours']['max']} (median {eng['totalHours']['median']})"],
        ["Pricing", f"{eng['pricing']['model'].title()} - ${eng['pricing']['amount']:,}"],
        ["Payment Terms", eng["pricing"]["paymentTerms"]],
        [],
    ]

    # Phase table header
    phase_header = ["Phase", "Start Date", "End Date", "Hours (Min)", "Hours (Max)", "Hours (Median)", "Status"]
    header_data.append(phase_header)

    total_min = 0
    total_max = 0
    total_med = 0

    for phase in plan["phases"]:
        p_min = sum(w["hours"]["min"] for w in phase["workstreams"])
        p_max = sum(w["hours"]["max"] for w in phase["workstreams"])
        p_med = sum(w["hours"]["median"] for w in phase["workstreams"])
        total_min += p_min
        total_max += p_max
        total_med += p_med
        header_data.append([
            f"Phase {phase['order']}: {phase['name']}",
            phase["startDate"],
            phase["endDate"],
            p_min, p_max, p_med,
            "Not Started"
        ])

    # Cross-phase
    for cw in plan.get("crossPhaseWorkstreams", []):
        total_min += cw["hours"]["min"]
        total_max += cw["hours"]["max"]
        total_med += cw["hours"]["median"]
        header_data.append([
            cw["name"],
            eng["startDate"],
            eng["estimatedEndDate"],
            cw["hours"]["min"], cw["hours"]["max"], cw["hours"]["median"],
            "Ongoing"
        ])

    # Totals
    header_data.append(["TOTAL", "", "", total_min, total_max, total_med, ""])

    header_data.append([])
    header_data.append(["ASSUMPTIONS"])
    for a in plan.get("assumptions", []):
        header_data.append([f"  - {a}"])

    header_data.append([])
    header_data.append(["EXCLUSIONS"])
    for e in plan.get("exclusions", []):
        header_data.append([f"  - {e}"])

    ws.update(header_data, "A1")

    # Formatting
    phase_header_row = 9  # Row where phase table starts
    ws.format(f"A1:B7", {
        "textFormat": {"bold": True},
    })
    ws.format(f"A{phase_header_row}:G{phase_header_row}", {
        "backgroundColor": {"red": 0.1, "green": 0.14, "blue": 0.49},
        "textFormat": {"bold": True, "foregroundColor": {"red": 1, "green": 1, "blue": 1}},
    })
    ws.freeze(rows=phase_header_row)


def build_phase_detail_tab(ws: Any, phase: Dict[str, Any]) -> None:
    """Build a phase detail tab."""
    # Header
    header = ["Workstream ID", "Workstream Name", "Tasks", "Hours (Min)", "Hours (Max)", "Hours (Median)", "Owner", "Status"]
    rows = [header]

    for workstream in phase["workstreams"]:
        tasks_text = "\n".join(f"{i+1}. {t}" for i, t in enumerate(workstream["tasks"]))
        rows.append([
            workstream["id"],
            workstream["name"],
            tasks_text,
            workstream["hours"]["min"],
            workstream["hours"]["max"],
            workstream["hours"]["median"],
            workstream["owner"],
            "Not Started",
        ])

    # Totals
    t_min = sum(w["hours"]["min"] for w in phase["workstreams"])
    t_max = sum(w["hours"]["max"] for w in phase["workstreams"])
    t_med = sum(w["hours"]["median"] for w in phase["workstreams"])
    rows.append(["", "TOTAL", "", t_min, t_max, t_med, "", ""])

    rows.append([])
    rows.append(["SUCCESS CRITERIA"])
    for sc in phase.get("successCriteria", []):
        rows.append([f"  - {sc}"])

    ws.update(rows, "A1")

    # Formatting
    ws.format("A1:H1", {
        "backgroundColor": {"red": 0.1, "green": 0.14, "blue": 0.49},
        "textFormat": {"bold": True, "foregroundColor": {"red": 1, "green": 1, "blue": 1}},
    })
    ws.freeze(rows=1)


def build_risk_tab(ws: Any, plan: Dict[str, Any]) -> None:
    """Build the Risk Register tab."""
    header = ["Risk ID", "Risk Name", "Severity", "Likelihood", "Impact", "Mitigation", "Owner"]
    rows = [header]

    for risk in plan.get("risks", []):
        rows.append([
            risk["id"],
            risk["name"],
            risk["severity"].title(),
            risk["likelihood"].title(),
            risk["impact"],
            risk["mitigation"],
            risk["owner"],
        ])

    rows.append([])
    rows.append(["CLIENT RESPONSIBILITIES"])
    for cr in plan.get("clientResponsibilities", []):
        rows.append([f"  - {cr}"])

    ws.update(rows, "A1")

    # Header formatting
    ws.format("A1:G1", {
        "backgroundColor": {"red": 0.1, "green": 0.14, "blue": 0.49},
        "textFormat": {"bold": True, "foregroundColor": {"red": 1, "green": 1, "blue": 1}},
    })
    ws.freeze(rows=1)

    # Conditional formatting for severity
    from gspread_formatting import ConditionalFormatRule, BooleanCondition, CellFormat, Color, get_conditional_format_rules, set_frozen_column_count
    # Note: gspread_formatting may not be installed; use basic approach instead
    # We'll apply severity colors via batch format calls
    for i, risk in enumerate(plan.get("risks", []), start=2):
        severity = risk["severity"]
        if severity == "high":
            color = {"red": 0.91, "green": 0.46, "blue": 0.47}
        elif severity == "medium":
            color = {"red": 0.98, "green": 0.91, "blue": 0.53}
        else:
            color = {"red": 0.58, "green": 0.84, "blue": 0.58}
        ws.format(f"C{i}", {"backgroundColor": color})


