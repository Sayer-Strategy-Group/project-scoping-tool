def main() -> None:
    parser = argparse.ArgumentParser(
        description="Create Google Sheets project tracker from plan.json"
    )
    parser.add_argument("client_dir",
                        help="Path to client folder containing plan.json")
    parser.add_argument("--dry-run", action="store_true",
                        help="Print planned actions without executing")
    parser.add_argument("--detail", action="store_true", default=True,
                        help="Generate all tabs (default)")
    parser.add_argument("--summary-only", action="store_true",
                        help="Generate only the Summary tab")
    parser.add_argument("--refresh", action="store_true",
                        help="Update existing sheet instead of creating new")
    parser.add_argument("--force", action="store_true",
                        help="Create new sheet even if one exists")
    args = parser.parse_args()

    client_dir = args.client_dir
    dry_run = args.dry_run

    # Load plan
    plan_path = os.path.join(client_dir, "plan.json")
    if not os.path.exists(plan_path):
        print(f"ERROR: {plan_path} not found")
        sys.exit(1)
    with open(plan_path, "r") as f:
        plan = json.load(f)

    client_name = plan["client"]["name"]
    engagement_name = plan["engagement"]["name"]
    sheet_title = f"{client_name} - {engagement_name} Tracker"

    # Idempotency check
    state = load_delivery_state(client_dir)
    existing_sheet = state.get("googleSheet", {}).get("sheetId")

    if existing_sheet and not args.force and not args.refresh:
        url = state["googleSheet"].get("sheetUrl", "unknown")
        print(f"Google Sheet already exists: {url}")
        print("Use --refresh to update or --force to create new.")
        sys.exit(0)

    # Determine tabs
    phases = plan["phases"]
    tab_names = ["Summary"]
    if not args.summary_only:
        for p in phases:
            tab_name = f"Phase {p['order']}: {p['name']}"
            # Sheets tab names max 100 chars
            if len(tab_name) > 100:
                tab_name = tab_name[:97] + "..."
            tab_names.append(tab_name)
        tab_names.append("Risk Register")

    prefix = "[DRY RUN] " if dry_run else ""

    print(f"\n{'='*60}")
    print(f"{prefix}GOOGLE SHEET: {sheet_title}")
    print(f"{'='*60}")
    print(f"Tabs: {len(tab_names)}")
    for t in tab_names:
        print(f"  - {t}")
    print(f"Share with: {plan['client']['contactEmail']}")
    if args.refresh and existing_sheet:
        print(f"Mode: Refresh existing ({existing_sheet})")
    else:
        print("Mode: Create new")
    print()

    if dry_run:
        # Print tab content preview
        print(f"{prefix}Summary tab:")
        print(f"  Header: client info, engagement details")
        print(f"  Phase table: {len(phases)} phases + cross-phase + totals")
        print(f"  Assumptions: {len(plan.get('assumptions', []))} items")
        print(f"  Exclusions: {len(plan.get('exclusions', []))} items")

        if not args.summary_only:
            for p in phases:
                ws_count = len(p["workstreams"])
                task_count = sum(len(w["tasks"]) for w in p["workstreams"])
                sc_count = len(p.get("successCriteria", []))
                print(f"\n{prefix}Phase {p['order']} detail tab:")
                print(f"  Workstreams: {ws_count}")
                print(f"  Tasks: {task_count}")
                print(f"  Success criteria: {sc_count}")

            print(f"\n{prefix}Risk Register tab:")
            print(f"  Risks: {len(plan.get('risks', []))}")
            print(f"  Client responsibilities: {len(plan.get('clientResponsibilities', []))}")

        print(f"\n{prefix}No changes made. Run without --dry-run to execute.")
        return

    # Real execution
    check_dependencies()

    import gspread
    from google.oauth2.service_account import Credentials

    sa_json = get_credential("GOOGLE_SERVICE_ACCOUNT_JSON")
    sa_info = json.loads(sa_json)
    scopes = [
        "https://www.googleapis.com/auth/spreadsheets",
        "https://www.googleapis.com/auth/drive",
    ]
    credentials = Credentials.from_service_account_info(sa_info, scopes=scopes)
    gc = gspread.authorize(credentials)

    if args.refresh and existing_sheet:
        print(f"Opening existing sheet: {existing_sheet}")
        spreadsheet = gc.open_by_key(existing_sheet)
    else:
        print(f"Creating sheet: {sheet_title}")
        spreadsheet = gc.create(sheet_title)

    # Create/get tabs
    existing_tabs = [ws.title for ws in spreadsheet.worksheets()]

    # Summary tab (rename Sheet1 if present)
    if "Sheet1" in existing_tabs:
        summary_ws = spreadsheet.worksheet("Sheet1")
        summary_ws.update_title("Summary")
    elif "Summary" in existing_tabs:
        summary_ws = spreadsheet.worksheet("Summary")
        summary_ws.clear()
    else:
        summary_ws = spreadsheet.add_worksheet("Summary", rows=100, cols=10)

    build_summary_tab(summary_ws, plan)
    print("  Built: Summary")

    if not args.summary_only:
        for p in phases:
            tab_name = f"Phase {p['order']}: {p['name']}"
            if len(tab_name) > 100:
                tab_name = tab_name[:97] + "..."
            if tab_name in existing_tabs:
                phase_ws = spreadsheet.worksheet(tab_name)
                phase_ws.clear()
            else:
                phase_ws = spreadsheet.add_worksheet(tab_name, rows=50, cols=10)
            build_phase_detail_tab(phase_ws, p)
            print(f"  Built: {tab_name}")

        # Risk Register
        if "Risk Register" in existing_tabs:
            risk_ws = spreadsheet.worksheet("Risk Register")
            risk_ws.clear()
        else:
            risk_ws = spreadsheet.add_worksheet("Risk Register", rows=50, cols=10)
        build_risk_tab(risk_ws, plan)
        print("  Built: Risk Register")

    # Share with client
    contact_email = plan["client"]["contactEmail"]
    try:
        spreadsheet.share(contact_email, perm_type="user", role="reader")
        print(f"  Shared with: {contact_email}")
    except Exception as e:
        print(f"  WARNING: Could not share with {contact_email}: {e}")

    sheet_url = spreadsheet.url
    print(f"\nSheet URL: {sheet_url}")

    # Save delivery state
    save_delivery_state(client_dir, {
        "clientName": client_name,
        "planJsonHash": plan_hash(plan_path),
        "googleSheet": {
            "sheetId": spreadsheet.id,
            "sheetUrl": sheet_url,
            "sharedWith": [contact_email],
            "createdAt": datetime.now(timezone.utc).isoformat() if not args.refresh else state.get("googleSheet", {}).get("createdAt", ""),
            "lastRefreshed": datetime.now(timezone.utc).isoformat() if args.refresh else None,
        },
    })


if __name__ == "__main__":
    main()
