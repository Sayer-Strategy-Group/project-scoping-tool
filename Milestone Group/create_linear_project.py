def get_credential(service_name: str) -> str:
    """Retrieve credential from macOS Keychain."""
    try:
        result = subprocess.check_output(
            ["security", "find-generic-password",
             "-a", "harbuckconsulting",
             "-s", service_name,
             "-w"],
            stderr=subprocess.DEVNULL
        )
        return result.decode("utf-8").strip()
    except subprocess.CalledProcessError:
        print(f"ERROR: Could not retrieve {service_name} from Keychain.")
        print(f"Add it with: security add-generic-password "
              f"-a harbuckconsulting -s {service_name} -w '<value>'")
        sys.exit(1)


