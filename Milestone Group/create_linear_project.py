def linear_request(
    query: str,
    variables: Optional[Dict[str, Any]] = None,
    api_key: str = "",
    dry_run: bool = False
) -> Dict[str, Any]:
    """Execute a Linear GraphQL request."""
    if dry_run:
        return {"data": {}}

    payload = json.dumps({
        "query": query,
        "variables": variables or {}
    }).encode("utf-8")

    req = urllib.request.Request(
        "https://api.linear.app/graphql",
        data=payload,
        headers={
            "Content-Type": "application/json",
            "Authorization": api_key,
        },
    )
    try:
        with urllib.request.urlopen(req) as resp:
            result = json.loads(resp.read().decode("utf-8"))
            if "errors" in result:
                print(f"Linear API error: {json.dumps(result['errors'], indent=2)}")
                sys.exit(1)
            return result
    except urllib.error.HTTPError as e:
        body = e.read().decode("utf-8") if e.fp else ""
        print(f"Linear API HTTP {e.code}: {body}")
        sys.exit(1)


def get_team(api_key: str, team_key: Optional[str], dry_run: bool) -> Tuple[str, str]:
    """Return (team_id, team_key). Auto-discovers first team if key not given."""
    if dry_run:
        return ("team-dry-run-id", team_key or "SAYER")

    query = "{ teams { nodes { id key name } } }"
    result = linear_request(query, api_key=api_key)
    teams = result.get("data", {}).get("teams", {}).get("nodes", [])
    if not teams:
        print("ERROR: No teams found in Linear workspace.")
        sys.exit(1)

    if team_key:
        for t in teams:
            if t["key"] == team_key:
                return (t["id"], t["key"])
        print(f"ERROR: Team '{team_key}' not found. Available: "
              f"{', '.join(t['key'] for t in teams)}")
        sys.exit(1)

    chosen = teams[0]
    print(f"Auto-selected team: {chosen['name']} ({chosen['key']})")
    return (chosen["id"], chosen["key"])


def get_or_create_label(
    api_key: str, team_id: str, name: str, color: Optional[str],
    existing_labels: Dict[str, str], dry_run: bool
) -> str:
    """Return label ID, creating if needed."""
    if name in existing_labels:
        return existing_labels[name]

    if dry_run:
        label_id = f"label-dry-run-{name}"
        existing_labels[name] = label_id
        return label_id

    mutation = """
    mutation CreateLabel($input: IssueLabelCreateInput!) {
        issueLabelCreate(input: $input) {
            issueLabel { id name }
            success
        }
    }
    """
    variables = {
        "input": {
            "teamId": team_id,
            "name": name,
        }
    }  # type: Dict[str, Any]
    if color:
        variables["input"]["color"] = color

    result = linear_request(mutation, variables, api_key=api_key)
    label_data = result.get("data", {}).get("issueLabelCreate", {})
    if not label_data.get("success"):
        print(f"WARNING: Could not create label '{name}'. Continuing without it.")
        return ""
    label_id = label_data["issueLabel"]["id"]
    existing_labels[name] = label_id
    return label_id


def fetch_existing_labels(api_key: str, team_id: str, dry_run: bool) -> Dict[str, str]:
    """Fetch all existing labels for the team. Returns {name: id}."""
    if dry_run:
        return {}
    query = """
    query($teamId: ID!) {
        issueLabels(filter: { team: { id: { eq: $teamId } } }) {
            nodes { id name }
        }
    }
    """
    result = linear_request(query, {"teamId": team_id}, api_key=api_key)
    labels = result.get("data", {}).get("issueLabels", {}).get("nodes", [])
    return {l["name"]: l["id"] for l in labels}


def create_project(
    api_key: str, team_id: str, name: str, description: str,
    start_date: str, target_date: str, dry_run: bool
) -> str:
    """Create a Linear project. Returns project ID."""
    if dry_run:
        return "project-dry-run-id"

    mutation = """
    mutation CreateProject($input: ProjectCreateInput!) {
        projectCreate(input: $input) {
            project { id url slugId }
            success
        }
    }
    """
    variables = {
        "input": {
            "name": name,
            "description": description,
            "teamIds": [team_id],
            "startDate": start_date,
            "targetDate": target_date,
        }
    }
    result = linear_request(mutation, variables, api_key=api_key)
    project_data = result.get("data", {}).get("projectCreate", {})
    if not project_data.get("success"):
        print("ERROR: Failed to create project.")
        sys.exit(1)
    return project_data["project"]["id"]


def get_project_url(api_key: str, project_id: str, dry_run: bool) -> str:
    """Get the project URL."""
    if dry_run:
        return "https://linear.app/harbuck/project/milestone-dry-run"
    query = """
    query($id: String!) {
        project(id: $id) { url }
    }
    """
    result = linear_request(query, {"id": project_id}, api_key=api_key)
    return result.get("data", {}).get("project", {}).get("url", "")


def create_issue(
    api_key: str, team_id: str, title: str, description: str,
    label_ids: List[str], project_id: str,
    parent_id: Optional[str], dry_run: bool
) -> str:
    """Create a Linear issue. Returns issue ID."""
    if dry_run:
        return f"issue-dry-run-{title[:20]}"

    mutation = """
    mutation CreateIssue($input: IssueCreateInput!) {
        issueCreate(input: $input) {
            issue { id identifier url }
            success
        }
    }
    """
    input_data = {
        "teamId": team_id,
        "title": title,
        "description": description,
        "projectId": project_id,
        "labelIds": [lid for lid in label_ids if lid],
    }  # type: Dict[str, Any]
    if parent_id:
        input_data["parentId"] = parent_id

    result = linear_request(mutation, {"input": input_data}, api_key=api_key)
    issue_data = result.get("data", {}).get("issueCreate", {})
    if not issue_data.get("success"):
        print(f"WARNING: Failed to create issue '{title}'.")
        return ""
    return issue_data["issue"]["id"]


