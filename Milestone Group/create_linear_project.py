def load_delivery_state(client_dir: str) -> Dict[str, Any]:
    """Load existing delivery-state.json or return empty dict."""
    state_path = os.path.join(client_dir, "delivery-state.json")
    if os.path.exists(state_path):
        with open(state_path, "r") as f:
            return json.load(f)
    return {}


def save_delivery_state(client_dir: str, updates: Dict[str, Any]) -> None:
    """Merge updates into delivery-state.json."""
    state = load_delivery_state(client_dir)
    for key, value in updates.items():
        if isinstance(value, dict) and isinstance(state.get(key), dict):
            state[key].update(value)
        else:
            state[key] = value
    state["lastUpdated"] = datetime.now(timezone.utc).isoformat()
    state_path = os.path.join(client_dir, "delivery-state.json")
    with open(state_path, "w") as f:
        json.dump(state, f, indent=2)
    print(f"Updated {state_path}")


def plan_hash(plan_path: str) -> str:
    """SHA-256 hash of plan.json for drift detection."""
    with open(plan_path, "rb") as f:
        return hashlib.sha256(f.read()).hexdigest()


